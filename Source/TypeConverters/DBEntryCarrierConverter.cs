using BriefingRoom4DCSWorld.DB;
using System.ComponentModel;
using System.Collections.Generic;
using System.Linq;

namespace BriefingRoom4DCSWorld.TypeConverters
{
    public class DBEntryCarrierConverter : StringConverter
    {
        public override bool GetStandardValuesSupported(ITypeDescriptorContext context)
        {
            return true;
        }

        public override bool GetStandardValuesExclusive(ITypeDescriptorContext context)
        {
            return true;
        }

        public override StandardValuesCollection GetStandardValues(ITypeDescriptorContext context)
        {
            List<string> carriers = new List<string> { "" };
            carriers.AddRange(Database.Instance.GetAllCarrierID());
            return new StandardValuesCollection(carriers.Distinct().OrderBy(x => x).ToArray());
        }
    }
}
